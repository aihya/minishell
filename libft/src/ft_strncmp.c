#include <string.h>

int		ft_strncmp(const char *s1, const char *s2, size_t n)
{
	if (n > 0)
	{
		while (n-- && (*s1 || *s2) && (*s1++ == *s2++))
			;
		return ((unsigned char)*(s1 - 1) - (unsigned char)*(s2 - 1));
	}
	return (0);
}
